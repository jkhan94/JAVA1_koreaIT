package day03;

public class Oper3 {

	public static void main(String[] args) {

		// 논리곱
		System.out.println(10 & 11);

		// 논리합
		System.out.println(10 | 11);

		// 배타논리합
		System.out.println(10 ^ 11);

		// 논리 부정
		System.out.println(~10);

		// 좌쉬프트 연산자
		// 2배
		System.out.println(10 << 1);

		// 우쉬프트 연산자
		// 1/2배
		System.out.println(10 >> 1);

	}

}
